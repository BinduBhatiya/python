# PRINT REVERCE TUPLE

# METHOD 1:
print("\nmethod 1 :")
tup = (7,8,3,2,1)
print('original value is: ',tup)
rev = tup[::-1]
print('reverced value is: ',rev)

# OUTPUT :
'''
original value is:  (7, 8, 3, 2, 1) 
reverced value is:  (1, 2, 3, 8, 7)
'''

# METHOD 2:
print("\nmethod 2 :")
tup = ('g','e','b','c')
