# FIND THE LENGTH OF LIST IN DIFFERENT WAY

# METHOD 1 (using enumerator) :
lst = [11,22,33]
sum = 0
for i,a in enumerate(lst):
    sum += 1
print('length of list is : ',sum)                                   # 3     



# METHOD 1 (using len() and len_hint() function) :
lst = [11,22,33]
print('length of list is : ',len(lst))                              # 3 
    




