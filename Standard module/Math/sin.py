# asin() :
# Return the arc sine of different numbers:

import math
print(math.asin(0.55))          # 0.5823642378687435
print(math.asin(-0.55))         # -0.5823642378687435
print(math.asin(0))             # 0.0
print(math.asin(1))             # 1.5707963267948966
print(math.asin(-1))            # -1.5707963267948966


'''
    1. The math.asin() method returns the arc sine of a number.

    2. Note: The parameter passed in math.asin() must lie between -1 to 1.

    3. Tip: math.asin(1) will return the value of PI/2, and math.asin(-1) will return the value of -PI/2.
'''

# asinh() :
# Return the inverse hyperbolic sine of numbers.

import math
print(math.asinh(7))            # 2.644120761058629
print(math.asinh(56))           # 4.718578581151767
print(math.asinh(2.45))         # 1.6284998192841909
print(math.asinh(1))            # 0.881373587019543
