# copysign()
# Return the value of the first parameter and the sign of the second parameter.
import math  

print(math.copysign(4, -1))         # -4.0
print(math.copysign(-8, 97.21))     # 8.0
print(math.copysign(-43, -76))      # -43.0
print(math.copysign(-4, 1))         # 4.0
print(math.copysign(43, 76))        # 43.0